
<div class="bg-blue-300 rounded-lg border border-blue-700 p-4">
    <h3 class="font-bold text-center">¿Qué es PlandyApp?</h3>
    <p class="my-2">PlandyApp es una aplicación para gestionar viajes en conjunto con más gente. Si decidís
        viajar a una casa rural, al extranjero o un evento improvisado estás en el sitio adecuado.
    </p>
    <p class="my-2">En la plataforma puedes crear 2 tipos de eventos, <span class="underline font-bold">CON PRESUPUESTO o SIN PRESUPUESTO</span>...</p>
    <ul class="list-disc mx-2"> 
        <li><span class="font-bold">Con Presupuesto</span> puedes estudiar un viaje y hacer una estimación de gastos (gasto de hotel, villetes de avión, comida...)
        estos no representan coste para ningún participante, solo es informativo.</li>
        <li><span class="font-bold">Sin Presupuesto</span> no tienes que pensar en qué se gastará, solamente presentas el gasto y los administradores lo aceptan o no.</li>
    </ul>
    <p class="my-2"><span class="font-bold">Gastos:</span> tienes un gasto como gasolina, lo presentas y esperas a que alguno de los administradores lo acepte. En el monento
        de aceptar el gasto en desglose de gastos se suma esa cantidad a tus gastos como participante.
    </p>
    <p class="my-2"><span class="font-bold">Gastos de Presupuesto:</span> solo está disponible para los eventos Con Presupuesto y solo los administradores pueden crear ese tipo de gasto.
        Los gastos de presupuesto no se contabilizan en la el estado de cuentas ya que es a modo informativo sobre cuánto se estiva que puede
        costar el viaje.
    </p>
    <p class="my-2"><span class="font-bold">Actividades:</span> además de gastos también puede haber actividades, en las que un administrador crea una actividad (ejemplo: submarinismo),
        con una fecha y coste por cabeza... son los participantes quienes deciden si quieren o no participar en cada una de esas actividades. 
    </p>
    <p class="my-2"><span class="font-bold">Estado de Cuentas:</span> te permite ver quién ha pagado y cuánto... hay una media en función de "pagado por participante" entre
        "gastos totales" y cuanto debe o se te debe en función de esa media.
    </p>
    <p class="my-2"><span class="font-bold">Contactos:</span> puedes buscar a tus contactos, amigos, familia que se hayan creado una cuenta, para buscar a un contacto debes conocer su 
        nombre de usuario completo. Esta aplicación no está pensada para tener contactos de manera masiva, para respetar la privacidad del resto
        de usuarios tu puedes agregar, eliminar o bloquear a contactos. *Actualmente se está desarrollando un método para reportar directamente
        dentro de la aplicación.
    </p>
    <p class="my-2"><span class="font-bold">Mensajería:</span> se dispone de mensajería	a través de chats (no instantáneos) para poder comunicarte con contactos y decidir sobre el nuevo
        evento que crear o bien, los chats de eventos en los que todos los participantes pueden comunicarse para decidir sobre sus inquietudes
        o gastos que se deben contemplar en la aplicación por ejemplo.
    </p>
</div>
